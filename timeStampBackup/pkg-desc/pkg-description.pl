Rozszerzenie to działa w ApacheOpenOffice i LibreOffice. Umożliwia użytkownikowi zapisanie bieżącego dokumentu w dwóch lokacjach jednocześnie:
	* Oryginału w miejscu przeznaczenia oraz 
	* Kopii o nazwie oryginalnego pliku uzupełnionej o znacznik czasowy.

Położenie kopii wynika z ustawień parametru kopii zapasowych w "Narzędzia → Opcje → OpenOffice/LibreOffice → Ścieżki → Kopie zapasowe".
Zaleca się dostosowanie ścieżki do folderu kopii zapasowych do przyjaznego miejsca.

Rozszerzenie dodaje dwa nowe polecenia w menu Plik oraz ikonę zapisu na standardowym pasku narzędzi.

